CREATE TABLE abc (
    id INTEGER PRIMARY KEY
);

CREATE SEQUENCE integers_01;

CREATE SEQUENCE integers_02 START 31
    OWNED BY abc.id;

CREATE SEQUENCE integers_03 AS INTEGER
    INCREMENT 10
    MINVALUE 100
    MAXVALUE 250000
    START 101
    CACHE 1
    NO CYCLE;

CREATE TEMPORARY SEQUENCE IF NOT EXISTS integers_04 AS SMALLINT
    INCREMENT BY 2
    NO MINVALUE
    NO MAXVALUE
    START WITH 3
    CYCLE;

