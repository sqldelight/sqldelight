CREATE TABLE A (id INTEGER, t TEXT);
CREATE TABLE B (id INTEGER, a_id INTEGER);
CREATE TABLE C (id INTEGER, a_id INTEGER);
CREATE TABLE D (id INTEGER, b_id INTEGER);

SELECT *
FROM A
LEFT JOIN B
ON A.id = B.a_id;

SELECT *
FROM A
LEFT OUTER JOIN B
ON A.id = B.a_id;

SELECT *
FROM A
RIGHT JOIN B
ON A.id = B.a_id;

SELECT *
FROM A
RIGHT OUTER JOIN B
ON A.id = B.a_id;

SELECT *
FROM A
FULL JOIN B
ON A.id = B.a_id;

SELECT *
FROM A
FULL OUTER JOIN B
ON A.id = B.a_id;

SELECT *
FROM A
CROSS JOIN B;

SELECT *
FROM A
NATURAL INNER JOIN B;

SELECT *
FROM A
NATURAL LEFT JOIN B;


